def check_dependencies():
    """Verifica dependencias del sistema avanzado"""
    print("\nüîç Verificando dependencias del sistema avanzado...")
    
    required_packages = [
        'feedparser',
        'beautifulsoup4',
        'lxml',
        'requests'
    ]
    
    missing = []
    
    for package in required_packages:
        try:
            __import__(package.replace('-', '_'))
            print(f"   ‚úÖ {package}")
        except ImportError:
            print(f"   ‚ùå {package} - FALTANTE")
            missing.append(package)
    
    if missing:
        print(f"\n‚ö†Ô∏è Dependencias faltantes: {', '.join(missing)}")
        print("üí° Instalando autom√°ticamente...")
        
        try:
            subprocess.run([
                sys.executable, '-m', 'pip', 'install'
            ] + missing, check=True, capture_output=True)
            print("‚úÖ Dependencias instaladas correctamente")
            return True
        except subprocess.CalledProcessError as e:
            print(f"‚ùå Error instalando dependencias: {e}")
            return False
    else:
        print("‚úÖ Todas las dependencias est√°n disponibles")
        return True

def start_backend():
    """Inicia el backend de ARIA"""
    print("\nüîß Iniciando backend de ARIA...")
    
    backend_path = Path(__file__).parent / "backend"
    os.chdir(backend_path)
    
    # Comando para iniciar el backend
    cmd = [sys.executable, "src/main_stable.py"]
    
    try:
        process = subprocess.Popen(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
            cwd=backend_path
        )
        
        print("‚úÖ Backend iniciado correctamente")
        return process
        
    except Exception as e:
        print(f"‚ùå Error iniciando backend: {e}")
        return None

def start_frontend():
    """Inicia el frontend de ARIA"""
    print("\nüé® Iniciando frontend de ARIA...")
    
    frontend_path = Path(__file__).parent / "frontend"
    
    try:
        process = subprocess.Popen(
            ["npm", "start"],
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
            cwd=frontend_path,
            shell=True
        )
        
        print("‚úÖ Frontend iniciado correctamente")
        return process
        
    except Exception as e:
        print(f"‚ùå Error iniciando frontend: {e}")
        return None

def wait_for_backend():
    """Espera a que el backend est√© disponible"""
    print("\n‚è≥ Esperando a que el backend est√© disponible...")
    
    max_attempts = 30
    for attempt in range(max_attempts):
        try:
            response = requests.get("http://localhost:8000/api/status", timeout=2)
            if response.status_code == 200:
                print("‚úÖ Backend disponible")
                return True
        except:
            pass
        
        print(f"   üîÑ Intento {attempt + 1}/{max_attempts}")
        time.sleep(2)
    
    print("‚ùå Backend no disponible despu√©s de 60 segundos")
    return False

def activate_advanced_learning():
    """Activa el sistema de aprendizaje avanzado"""
    print("\nüß† Activando sistema de aprendizaje avanzado...")
    
    try:
        # Verificar capacidades
        response = requests.get("http://localhost:8000/api/advanced_learning/capabilities")
        
        if response.status_code == 200:
            capabilities = response.json()
            
            if capabilities.get("available"):
                print("‚úÖ Sistema avanzado disponible")
                
                # Mostrar capacidades
                features = capabilities.get("features", {})
                available_features = [k for k, v in features.items() if v]
                print(f"   üîπ Caracter√≠sticas activas: {len(available_features)}")
                
                sources = capabilities.get("sources", [])
                print(f"   üîπ Fuentes de conocimiento: {len(sources)}")
                
                # Iniciar aprendizaje
                start_response = requests.post("http://localhost:8000/api/auto_learning/start")
                
                if start_response.status_code == 200:
                    result = start_response.json()
                    
                    if result.get("success"):
                        system_type = result.get("system", "unknown")
                        print(f"‚úÖ Sistema {system_type} iniciado correctamente")
                        
                        if system_type == "advanced":
                            print("üöÄ ¬°SISTEMA AVANZADO ACTIVO!")
                            print("   ‚ú® Superando limitaciones anteriores:")
                            print("   ‚ùå ¬°YA NO es simulado!")
                            print("   ‚úÖ Acceso a internet en tiempo real")
                            print("   ‚úÖ Lee fuentes externas verificadas")
                            print("   ‚úÖ APIs cient√≠ficas y noticias")
                            print("   ‚úÖ Conocimiento real y actualizado")
                        
                        return True
                    else:
                        print(f"‚ùå Error iniciando: {result.get('message')}")
                        return False
                else:
                    print(f"‚ùå Error HTTP {start_response.status_code}")
                    return False
            else:
                print("‚ö†Ô∏è Sistema avanzado no disponible - usando sistema b√°sico")
                return False
        else:
            print(f"‚ùå Error verificando capacidades: HTTP {response.status_code}")
            return False
            
    except Exception as e:
        print(f"‚ùå Error activando sistema avanzado: {e}")
        return False

def open_browser():
    """Abre el navegador autom√°ticamente"""
    print("\nüåê Abriendo navegador...")
    
